const translations: Record<LanguageCode, TranslationDictionary> = {
  en: englishTranslations,
  it: italianTranslations,
  fr: frenchTranslations,
  es: spanishTranslations
};

function interpolate(template: string, variables?: Record<string, string | number>): string {
  if (!variables) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = variables[key];
    return value === undefined ? "" : String(value);
  });
}

export function normalizeLanguage(input: string | null | undefined): LanguageCode | null {
  if (!input) {
    return null;
  }

  const normalized = input.toLowerCase().split(/[-_]/)[0];
  return supportedLanguages.some((item) => item.code === normalized)
    ? (normalized as LanguageCode)
    : null;
}

export function detectBrowserLanguage(
  navigatorLike:
    | {
        language?: string;
        languages?: readonly string[];
      }
    | undefined = typeof navigator === "undefined" ? undefined : navigator
): LanguageCode {
  const browserCandidates = navigatorLike?.languages?.length
    ? navigatorLike.languages
    : navigatorLike?.language
      ? [navigatorLike.language]
      : [];

  for (const candidate of browserCandidates) {
    const normalized = normalizeLanguage(candidate);
    if (normalized) {
      return normalized;
    }
  }

  return "en";
}

export function loadStoredLanguage(
  storage: Pick<Storage, "getItem"> | undefined =
    typeof window === "undefined" ? undefined : window.localStorage
): LanguageCode | null {
  const raw = storage?.getItem(LANGUAGE_STORAGE_KEY) ?? null;
  return normalizeLanguage(raw);
}

export function persistLanguage(
  language: LanguageCode,
  storage: Pick<Storage, "setItem"> | undefined =
    typeof window === "undefined" ? undefined : window.localStorage
): void {
  storage?.setItem(LANGUAGE_STORAGE_KEY, language);
}

export function resolveInitialLanguage(
  options?: {
    storage?: Pick<Storage, "getItem">;
    navigator?: {
      language?: string;
      languages?: readonly string[];
    };
  }
): LanguageCode {
  const stored = loadStoredLanguage(options?.storage);
  if (stored) {
    return stored;
  }

  return detectBrowserLanguage(options?.navigator);
}

export function translate(
  language: LanguageCode,
  key: TranslationKey,
  variables?: Record<string, string | number>
): string {
  return interpolate(translations[language][key], variables);
}

interface I18nContextValue {
  language: LanguageCode;
  setLanguage: Dispatch<SetStateAction<LanguageCode>>;
  t: (key: TranslationKey, variables?: Record<string, string | number>) => string;
  formatCurrency: (amount: number, currency: string) => string;
  formatDate: (date: string) => string;
  formatDecisionLabel: (value: "safe" | "caution" | "hold") => string;
  formatTransactionDirection: (value: "income" | "expense") => string;
  formatTransactionCategory: (value: "essential" | "committed") => string;
  formatRecurringCadence: (value: "daily" | "weekly" | "monthly") => string;
  formatGoalKind: (value: "goal" | "safety_buffer") => string;
  formatConnectionStatus: (value: string) => string;
  formatSourceLabel: (value: string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider(props: { children: ReactNode }) {
  const [language, setLanguage] = useState<LanguageCode>(() => resolveInitialLanguage());

  useEffect(() => {
    persistLanguage(language);
    setApiLanguage(language);
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: TranslationKey, variables?: Record<string, string | number>) =>
      translate(language, key, variables);
    const translateAny = (key: string) => t(key as TranslationKey);

    return {
      language,
      setLanguage,
      t,
      formatCurrency: (amount, currency) =>
        formatCurrencyValue(amount, currency, language),
      formatDate: (date) => formatDateValue(date, language),
      formatDecisionLabel: (value) => formatDecisionLabelValue(value, translateAny),
      formatTransactionDirection: (value) =>
        formatTransactionDirectionValue(value, translateAny),
      formatTransactionCategory: (value) =>
        formatTransactionCategoryValue(value, translateAny),
      formatRecurringCadence: (value) =>
        formatRecurringCadenceValue(value, translateAny),
      formatGoalKind: (value) => formatGoalKindValue(value, translateAny),
      formatConnectionStatus: (value) => formatConnectionStatusValue(value, translateAny),
      formatSourceLabel: (value) => formatSourceLabelValue(value, translateAny)
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{props.children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider.");
  }

  return context;
}
